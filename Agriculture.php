          <p style="text-align: justify;"><br /><br />Objective of HND-AGRI course is to produce middle level managers, who are capable of practicing appropriate technology 
            and management tasks in crop and animal production, agro product processing and agribusiness and contributing to the socioeconomic and sustainable 
            development of the agriculture sector locally and internationally, with professional attitudes and confidence to serve the local and global community.
          </p>
          <h3 style="text-align: justify;">Overview of the Program</h3>
          <ul>
          <li style="text-align: justify;">HNDT (Agri) program is a diploma program of three year duration.</li>
          <li style="text-align: justify;">The program is offered in course unit system in semesters. The program comprises 6 semesters, each of 15 weeks duration.</li>
          <li style="text-align: justify;">The total program consists of 101 credits.</li>
          <li style="text-align: justify;">Out of the 101 credits, 60 credits contribute to the GPA of the program, while the rest 41 credits, which require to be completed, are NGPA.</li>
          <li style="text-align: justify;">The program consists of four semesters of course work and two semesters of in-plant training.</li>
          </ul>
